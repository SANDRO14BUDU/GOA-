#მომხმარებეკს რიცხვები შეყავს რომ გამოითვალოს პროგრამამ
num1 = float(input("შეიყვანეთ პირველი რიცხვი"))
num2 = float(input("შეიყვანეთ მეორე რიცხვი"))

#აქ ხდება ოპერაცია მომხმარებლის შეყვანილი რიცხვებით (+ , - , * , /)
pliusi = num1 + num2
sxvaoba = num1 - num2
gamravleba = num1 * num2
gayofa = num1 / num2

#აქ იპრინტება "ჯამი , სხვაობა , ნამრავლი და გაყოფა"
print("ჯამი:", pliusi)
print("სხვაობა:", sxvaoba)
print("ნამრავლი:", gamravleba)
print("განაყოფი:", gayofa)


